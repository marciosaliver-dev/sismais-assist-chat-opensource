// ── Helpers ──────────────────────────────────────────────

export function getTicketById(id: string): AtendimentoTicket | undefined {
  return mockTickets.find(t => t.id === id)
}

export function getMessagesForTicket(ticketId: string): AtendimentoMessage[] {
  return mockMessages[ticketId] || defaultMessages(mockTickets.find(t => t.id === ticketId)!)
}

export function getTicketsByStatus(status: string): AtendimentoTicket[] {
  return mockTickets.filter(t => t.status === status)
}

