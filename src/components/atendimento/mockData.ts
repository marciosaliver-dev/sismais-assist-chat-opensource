// ── Flow Steps ───────────────────────────────────────────

export const getFlowSteps = (status: string): FlowStep[] => {
  const steps: FlowStep[] = [
    { id: 'fila', label: 'Fila', status: 'pending' },
    { id: 'em_atendimento', label: 'Em Atendim.', status: 'pending' },
    { id: 'ag_cliente', label: 'Aguard. Cliente', status: 'pending' },
    { id: 'ag_interno', label: 'Aguard. Interno', status: 'pending' },
    { id: 'concluido', label: 'Concluído', status: 'pending' },
  ]

  const statusOrder = ['fila', 'em_atendimento', 'ag_cliente', 'ag_interno', 'concluido']
  const currentIdx = statusOrder.indexOf(status)

  return steps.map((step, i) => ({
    ...step,
    status: i < currentIdx ? 'done' : i === currentIdx ? 'current' : 'pending',
  }))
}

